async function obterCoordenadas(endereco) {
  try {
    const url = `https://geocode.xyz/${encodeURIComponent(
      endereco
    )}&auth=62393792722535545847x44046?json=1`;
    const response = await fetch(url);
    const data = await response.json();
    if (data.latt && data.longt) {
      const latitude = data.latt;
      const longitude = data.longt;
      return { latitude, longitude };
    } else {
      throw new Error("Endereço não encontrado");
    }
  } catch (error) {
    console.error("Erro ao obter coordenadas:", error);
    return null;
  }
}

// Exemplo de uso da função
async function addLatAndLonToFeiras(feiras) {
  for (const feira of feiras) {
    if (feira.latitude !== "Throttled! See geocode.xyz/pricing") {
      continue;
    }
    const endereco = `${feira.endereco}, ${feira.bairro}, Rio de Janeiro`;

    try {
      const coordenadas = await obterCoordenadas(endereco);

      if (coordenadas) {
        feira.latitude = coordenadas.latitude;
        feira.longitude = coordenadas.longitude;
      } else {
        console.log(
          `Coordenadas não encontradas para a feira da rua ${feira.endereco}`
        );
      }
    } catch (error) {
      console.log(`Erro na feira ${feira.endereco}`, error);
    }
  }

  console.log("Todos os processamentos concluídos!");
  return feiras;
}

