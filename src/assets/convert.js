const timeoutDelay = 10000; // Tempo limite em milissegundos (exemplo: 10 segundos)

async function obterCoordenadas(feira) {
  const endereco = `${feira.endereco}, ${feira.bairro}, Rio de Janeiro`;

  try {
    const url = `https://nominatim.openstreetmap.org/search?format=json&q=${encodeURIComponent(
      endereco
    )}`;
    const response = await fetch(url, { timeout: timeoutDelay });

    if (!response.ok) {
      throw new Error(`Erro na requisição: ${response.statusText}`);
    }

    const data = await response.json();

    if (data && data.length > 0) {
      const { lat, lon } = data[0];
      return { latitude: lat, longitude: lon };
    } else {
      throw new Error("Endereço não encontrado");
    }
  } catch (error) {
    if (error.name === "AbortError") {
      console.error(`Timeout ao tentar obter coordenadas para ${endereco}`);
    } else {
      console.error("Erro ao obter coordenadas:", error);
    }
    return null;
  }
}

// Exemplo de uso da função
async function addLatAndLonToFeiras(feiras) {
  for (let i = 0; i < feiras.length; i++) {
    const feira = feiras[i];

    try {
      const coordenadas = await obterCoordenadas(feira);

      if (coordenadas) {
        feira.latitude = coordenadas.latitude;
        feira.longitude = coordenadas.longitude;
      } else {
        console.log(
          `Coordenadas não encontradas para a feira da rua ${feira.endereco}`
        );
      }
    } catch (error) {
      console.log(`Erro na feira ${feira.endereco}`, error);
    }

    console.log(`Processamento da feira ${i + 1} concluído.`);
  }

  console.log("Todos os processamentos concluídos!");
  return feiras;
}

